public class userev {
    private String fn,ln,em,mn,co,br,even;
    public userev(String fn,String ln,String em,String mn,String co,String br,String even)
    {
        this.fn=fn;
        this.ln=ln;
        this.em=em;
        this.mn=mn;
        this.co=co;
        this.br=br;
        this.even=even;
    }

    
    public String  getfn()
    {
        return fn;
    }
    public String  getln()
    {
        return ln;
    }
    public String  getem()
    {
        return em;
    }
    public String  getmn()
    {
        return mn;
    }
    public String  getco()
    {
        return co;
    }
    public String  getbr()
    {
        return br;
    }
     public String  geteven()
    {
        return even;
    }
}
