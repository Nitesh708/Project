     public ArrayList<User> userList()
   {
       ArrayList<User> usersList=new ArrayList<>();
       try{ 
         Connection con=null;
         Class.forName("com.mysql.jdbc.Driver");
        con=DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","mysqlnitisha");
        String query="select * from registerco";
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery(query);
        User user;
        while(rs.next())
        {
            user=new User(rs.getString("First_name"),rs.getString("Last_name"),rs.getString("Email"),rs.getString("mobile"),rs.getString("branch"),rs.getString("nom"),rs.getString("comp"));
            usersList.add(user);
         }
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
       return usersList;
   }
       public void show_user()
      {
       ArrayList<User> list= userList();
       DefaultTableModel model=(DefaultTableModel)jTable2.getModel();
       Object[] row=new Object[7];
       for(int i=0;i<list.size();i++)
       {
           row[0]=list.get(i).getfn();
           row[1]=list.get(i).getln();
           row[2]=list.get(i).getem();
           row[3]=list.get(i).getmn();
           row[4]=list.get(i).getbr();
           row[5]=list.get(i).getnom();
           row[6]=list.get(i).getcomp();
           model.addRow(row);
       }
   
    }
       
